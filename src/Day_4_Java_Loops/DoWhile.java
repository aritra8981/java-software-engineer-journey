package Day_4_Java_Loops;

public class DoWhile {
    static void main() {

        int i = 1;  //Initialization

        do {
            System.out.println(i);
            i++;    //Update
        } while (i <= 10);  //Condition



        int j = 1;  //Initialization

        do {
            System.out.println(j);
            j++;    //Update
        } while (j < 0);  //Condition
    }
}
