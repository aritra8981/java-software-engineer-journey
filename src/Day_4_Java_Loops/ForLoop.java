package Day_4_Java_Loops;

public class ForLoop {
    static void main() {

        //First Loop
        for (int i=1; i<=5; i++){
            System.out.println("Value of I is : " + i);
        }

        //Second Loop
        for (int i = 1; i<=10; i++){
            System.out.println("Aritra is the best");
        }

        //Third Loop
        for (int i=1; i<=10; i+=2){
            System.out.println(i);
        }
        System.out.println();

        //Fourth Loop
        for (int i=2; i<=10; i+=2){
            System.out.println(i);
        }
    }
}
