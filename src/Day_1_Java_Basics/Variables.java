package Day_1_Java_Basics;

public class Variables {
    static void main() {

        //Create //declare
        int age;
        //System.out.println(age);
        //assign value
        age = 10;
        age = 11;
        System.out.println(age);

        //define or initialize
        int totalMarks =20;
        System.out.println(totalMarks);

        //Case Sensitive
        int weight = 80;
        int WEIGHT = 100;
        System.out.println("weight: " + weight);
        System.out.println("WEIGHT: " + WEIGHT);

        //Starting Letter
        int marks = 10;
        int MARKS = 11;
        int _marks = 12;
        int $marks = 13;

        //Subsequent Letters
        int height5 = 21;
        int height_love = 33;
        int height$12love = 44;
        int height_ = 22;
        int height$ = 23;

        //No Reserved Keywords
        //int class = 33;

        //Length
        int helloAritraHowAreYou = 54;
        int a = 1;
        int b = 2;
        int c = 3;

        //CamelCase
        int myNameIsAritra = 3;
        int DAYS_IN_YEAR = 365;


    }
}

